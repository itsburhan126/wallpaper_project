  static String getText(String localeCode, String key) {
    if (_localizedValues.containsKey(localeCode) && _localizedValues[localeCode]!.containsKey(key)) {
      return _localizedValues[localeCode]![key]!;
    }
    if (_localizedValues['en']!.containsKey(key)) {
      return _localizedValues['en']![key]!;
    }
    // Fallback to formatted key to avoid ugly snake_case in UI
    return key.replaceAll('_', ' ').split(' ').map((str) => str.isNotEmpty ? '${str[0].toUpperCase()}${str.substring(1)}' : '').join(' ');
  }
